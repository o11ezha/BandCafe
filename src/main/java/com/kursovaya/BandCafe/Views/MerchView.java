package com.kursovaya.BandCafe.Views;

public class MerchView {

    String merchID;
    String merchName;
    Float merchPrice;
    Boolean merchStatus;
    String merchDesc;
    String merchGroupName;
    String merchFandomName;

    public String getMerchID() {
        return merchID;
    }

    public void setMerchID(String merchID) {
        this.merchID = merchID;
    }

    public String getMerchName() {
        return merchName;
    }

    public void setMerchName(String merchName) {
        this.merchName = merchName;
    }

    public Float getMerchPrice() {
        return merchPrice;
    }

    public void setMerchPrice(Float merchPrice) {
        this.merchPrice = merchPrice;
    }

    public Boolean getMerchStatus() {
        return merchStatus;
    }

    public void setMerchStatus(Boolean merchStatus) {
        this.merchStatus = merchStatus;
    }

    public String getMerchDesc() {
        return merchDesc;
    }

    public void setMerchDesc(String merchDesc) {
        this.merchDesc = merchDesc;
    }

    public String getMerchGroupName() {
        return merchGroupName;
    }

    public void setMerchGroupName(String merchGroupName) {
        this.merchGroupName = merchGroupName;
    }

    public String getMerchFandomName() {
        return merchFandomName;
    }

    public void setMerchFandomName(String merchFandomName) {
        this.merchFandomName = merchFandomName;
    }
}
